#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int z;

	i = 0;
	z = 0;
	while (s[i] != '\0' && z == 0)
	{
		z++;
		i++;
	}
	z = z + i;
	while (i >= 0 && z >= 0)
	{
		if (s[i] == c)
			return ((char *)(s + i));
		i--;
		z++;
	}
	return (0);
}
